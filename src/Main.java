public class Main {
    public static void main(String[] args) {
        //task1
        for (int i = 1; i <= 10; i ++) {
            System.out.println( "Итерация цикла " +i);
        }
        //task 2
        for (int i = 10; i >= 0; i --) {
            System.out.println( "Итерация цикла " +i);
        }
        //task 3
        for (int i = 0; i <= 17; i = i + 2) {
            System.out.println( "Итерация цикла " +i);
        }
        //task 4
        for (int i = 10; i >= -10; i --) {
            System.out.println( "Итерация цикла " +i);
        }
    }
}